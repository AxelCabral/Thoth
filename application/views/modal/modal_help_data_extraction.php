				<p>The data extraction forms must be designed to collect all the information needed to address the
					review
					questions and the study quality criteria. If the quality criteria are to be used to identify
					inclusion/exclusion criteria, they require separate forms (since the information must be collected
					prior
					to the main data extraction exercise). If the quality criteria are to be used as part of the data
					analysis, the quality criteria and the review data can be included in the same form.</p>

				<p>In most cases, data extraction will define a set of numerical values that should be extracted for
					each
					study (e.g. number of subjects, treatment effect, confidence intervals, etc.). Numerical data are
					important for any attempt to summarise the results of a set of primary studies and are a
					prerequisite
					for meta-analysis (i.e. statistical techniques aimed at integrating the results of the primary
					studies).</p>


				<p>The extraction quest should be defined from a description and the type of data. The data types
					are:</p>
				<ul>
					<li>Text: The extracted data is defined through simple text, giving the researcher the freedom to
						define
						the extracted data.
					</li>

					<li>Pick One List: Through a predefined list of data, the researcher should choose only one data
						extraction option.
					</li>

					<li>Multiple Choice List: Through a predefined list of data, the researcher can choose more than one
						data
						extraction option.
					</li>
				</ul>

				<p>Example:</p>

				<p><strong>Description:</strong> Abstract - <strong>Type of data:</strong> Text.</p>

				<p><strong>Description:</strong> Database - <strong>Type of data:</strong> Pick One List. (List: ACM, IEEE, Scopus)</p>
