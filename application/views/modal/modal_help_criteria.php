				<p><strong>All:</strong> The study must contain all the inclusion or exclusion criteria.</p>

				<p><strong>At Least:</strong> The study should contain at least the criteria selected.</p>

				<p><strong>Any:</strong> The study may contain any of the criteria.</p>

