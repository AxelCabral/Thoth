				<p>Set a small identifier for the include/exclude criteria.</p>
				<p>Study selection criteria are intended to identify those primary studies that provide direct evidence
					about the research question. In order to reduce the likekihood of bias, selection criteria should be
					decided during the protocol definition, although they may be refined during the search process.</p>
				<p>Inclusion and Exclusion criteria should be based on the research question. They should be piloted to
					ensure that they can be reliably interpreted and that they classify studies correctly.</p>
				<p>Example:</p>

				<p><strong>Inclusion Criteria:</strong> IC1 The publication should propose a tool to support the
					performance test.</p>

				<p><strong>Exclusion Criteria:</strong> EC1 Duplicate articles.</p>

				<p>For a study to be evaluated by criteria, it is necessary to define a rule for acceptance / rejection.</p>

